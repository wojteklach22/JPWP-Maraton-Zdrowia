package jpwp.projekt.maraton_zdrowia;

import android.widget.ImageView;

/**
 * Klasa odpowiadająca za stworzenie obiektu odpowiedzialnego za wyświetlanie zdjęcia jedzenia
 */
class Jedzenie {
    /**
     * Zdjęcie jedzenia
     */
    public ImageView zdj_jedzenia;
    /**
     * Określenie czy jedzenie jest zdrowe czy niezdrowe
     */
    public boolean zdrowe;
    /**
     * Określenie na której bieżni znajduje się jedzenie
     */
    public int bieznia;
}